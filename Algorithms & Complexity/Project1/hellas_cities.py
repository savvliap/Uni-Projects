def get_cities_distances_graph_filtered(dist):
    g = nx.Graph()

    g.add_nodes_from(list(Cities))

    length = len(Cities)

    for x in range(length):
        for y in range(length):
            if Distances[x][y] <= dist:
                g.add_edge(Cities[x], Cities[y], weight=Distances[x][y])

    return g

def get_cities_distances_graph():
    return get_cities_distances_graph_filtered(float('inf'))

